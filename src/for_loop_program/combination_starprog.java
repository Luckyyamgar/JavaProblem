package for_loop_program;

public class combination_starprog {

	public static void main(String[] args) {
		// combination program     IMP
		int n=5;
		for(int i=1;i<=n;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print("1");
			}System.out.println(" ");
		}

		for(int i=1;i<=5;i++) {
		for(int j=5;j>=i;j--) {
			System.out.print("1");
		}System.out.println(" ");
		}
		
		
		
	}

}
